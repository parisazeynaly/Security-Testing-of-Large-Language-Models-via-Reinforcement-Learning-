class PAGGuidedPolicy:
    """
    THE MISSING PIECE — This is what the professor asked for.

    "Use the causal model as GUIDANCE to generate attack prompts."

    ═══════════════════════════════════════════════════════════
    CORRECTED PAG STRUCTURE (3 direct parents of y):
    ═══════════════════════════════════════════════════════════

    Direct parents of y (Markov Blanket of y):
      hypothetical_framing  → y  (primary bridge node)
      imperative_tone       → y  (independent direct cause)
      instructional_style   → y  (third direct cause ← was missing!)

    Indirect path (mediator):
      instructional_style → obfuscation_techniques → hypothetical_framing → y

    Tone influencers (upstream of imperative_tone):
      malicious_intent           → imperative_tone
      responsibility_externalization → imperative_tone

    Synergy Peak:
      imperative_tone + hypothetical_framing = independent direct parents
      → combining them = high-probability success region

    Full PAG:
    malicious_intent ──────────────────────────────────────────────┐
    responsibility_externalization ────────────────────────────────┤
                                                                    ▼
    instructional_style ──► obfuscation_techniques ──► hypothetical_framing ──► y
          │                                                                      ▲
          └──────────────────────────────────────────────────────────────────────┘
                                    imperative_tone ───────────────────────────► y
    ═══════════════════════════════════════════════════════════
    """

    # CORRECTED: 3 direct parents of y (full Markov Blanket)
    DIRECT_PARENTS_OF_Y = [
        "hypothetical_framing",   # primary bridge, highest weight
        "imperative_tone",        # independent direct cause
        "instructional_style",    # third direct cause (was missing!)
    ]

    # Priority weights for direct parents (from PAG edge strength)
    # hypothetical_framing highest because obfuscation also flows through it
    PARENT_PRIORITY = {
        "hypothetical_framing": 1.0,   # primary — all indirect paths go through it
        "imperative_tone":      0.8,   # strong but caught easily by safety filters
        "instructional_style":  0.6,   # direct but also has indirect path
    }

    # Causal path: which factor CAUSES which (indirect paths)
    PAG_CAUSES = {
        "hypothetical_framing":   "obfuscation_techniques",  # obfuscation → framing
        "imperative_tone":        None,                       # no upstream in our obs space
        "obfuscation_techniques": "instructional_style",      # instructional → obfuscation
        "instructional_style":    None,                       # root node
    }

    # Which mutator targets which PAG node
    FACTOR_TO_ACTION = {
        "hypothetical_framing":   "causal_framing",
        "imperative_tone":        "causal_tone",
        "obfuscation_techniques": "causal_obfuscation",
        "instructional_style":    "causal_complexity",
    }

    # Threshold below which a factor is considered "weak"
    WEAK_THRESHOLD = 0.5

    # Synergy threshold — if both framing AND tone are strong → combined attack
    SYNERGY_THRESHOLD = 0.7

    def select(self,
               obs: np.ndarray,
               ppo_action: int,
               mut_keys: list,
               epsilon: float = 0.3,
               action_history: list = None) -> int:
        """
        PAG-guided action selection using full Markov Blanket of y.

        Strategy:
        1. Check Synergy Peak first: framing + tone both strong → combined
        2. Find weakest direct parent of y (priority-weighted)
        3. Traverse causal path upstream if needed
        4. 30% of time: let PPO explore freely (epsilon-greedy)
        5. If action collapse detected → force PAG override
        """
        # Force override if action collapsing
        if action_history and len(action_history) >= 3:
            last_3 = action_history[-3:]
            if len(set(last_3)) == 1:
                epsilon = 0.0  # force PAG when collapsing

        # Epsilon-greedy: let PPO explore sometimes
        if random.random() < epsilon:
            return ppo_action

        obs_dict = dict(zip(OBS_KEYS, obs))

        # ── Step 1: Check Synergy Peak ─────────────────────────
        # If framing + tone are both strong → combined attack
        framing = obs_dict.get("hypothetical_framing", 0.0)
        tone    = obs_dict.get("imperative_tone", 0.0)
        if framing >= self.SYNERGY_THRESHOLD and tone >= self.SYNERGY_THRESHOLD:
            action_name = "causal_combined"
            if action_name in mut_keys:
                # print(f"    🎯 PAG SYNERGY: framing={framing:.2f} + tone={tone:.2f} → causal_combined")
                return mut_keys.index(action_name)

        # ── Step 2: Find weakest direct parent of y ────────────
        # Weight by priority — framing > tone > instructional
        parent_scores = {}
        for f in self.DIRECT_PARENTS_OF_Y:
            score = obs_dict.get(f, 0.0)
            priority = self.PARENT_PRIORITY[f]
            # Effective weakness = (1 - score) * priority
            # Higher = more urgent to fix
            parent_scores[f] = (1.0 - score) * priority

        weakest_parent = max(parent_scores, key=parent_scores.get)
        weakest_score  = obs_dict.get(weakest_parent, 0.0)

        # ── Step 3: Traverse causal path if needed ─────────────
        if weakest_score < self.WEAK_THRESHOLD:
            upstream = self.PAG_CAUSES.get(weakest_parent)
            if upstream and obs_dict.get(upstream, 1.0) < self.WEAK_THRESHOLD:
                # Upstream also weak → fix upstream first (causal path)
                target_factor = upstream
                # Check one more level up
                upstream2 = self.PAG_CAUSES.get(upstream)
                if upstream2 and obs_dict.get(upstream2, 1.0) < self.WEAK_THRESHOLD:
                    target_factor = upstream2
            else:
                # Upstream fine → fix the direct parent directly
                target_factor = weakest_parent
        else:
            # All direct parents reasonably strong → combined
            action_name = "causal_combined"
            if action_name in mut_keys:
                return mut_keys.index(action_name)
            return ppo_action

        # ── Step 4: Map to action ──────────────────────────────
        action_name = self.FACTOR_TO_ACTION.get(target_factor)
        if action_name and action_name in mut_keys:
            guided_action = mut_keys.index(action_name)
            # print(f"    🎯 PAG: {target_factor}={obs_dict.get(target_factor, 0):.2f} "
            #       f"(priority={self.PARENT_PRIORITY.get(target_factor, 0):.1f}) → {action_name}")
            return guided_action

        return ppo_action

    def explain(self, obs: np.ndarray) -> str:
        """Returns human-readable explanation of PAG decision."""
        obs_dict = dict(zip(OBS_KEYS, obs))
        lines = ["PAG Analysis (Full Markov Blanket of y):"]
        lines.append("  Direct parents of y:")
        for f in self.DIRECT_PARENTS_OF_Y:
            score = obs_dict.get(f, 0.0)
            priority = self.PARENT_PRIORITY[f]
            status = "✅ strong" if score >= self.WEAK_THRESHOLD else "⚠️ WEAK"
            lines.append(f"    {f}: {score:.2f} {status} (priority={priority})")
        lines.append("  Mediators:")
        for f in ["obfuscation_techniques"]:
            score = obs_dict.get(f, 0.0)
            status = "✅ strong" if score >= self.WEAK_THRESHOLD else "⚠️ WEAK"
            lines.append(f"    {f}: {score:.2f} {status} (mediator)")
        framing = obs_dict.get("hypothetical_framing", 0.0)
        tone    = obs_dict.get("imperative_tone", 0.0)
        if framing >= self.SYNERGY_THRESHOLD and tone >= self.SYNERGY_THRESHOLD:
            lines.append("  ⚡ SYNERGY PEAK DETECTED → causal_combined")
        return "\n".join(lines)


