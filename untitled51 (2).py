class CausalRLBreakerEnv(gym.Env):
    metadata = {"render_modes": []}

    def __init__(self, questions, reference_answers,
                 similarity_model=None,
                 max_steps=12,
                 cache_max=10_000,
                 train_mode=True,
                 pag_epsilon=0.5):       # NEW: PAG guidance strength
        super().__init__()

        self.proxy            = CAUSAL_PROXY
        self.causal_generator = CausalGuidedGenerator(use_llm_rewrite=False)
        self.pag_policy       = PAG_POLICY       # NEW: PAG policy
        self.pag_epsilon      = pag_epsilon      # NEW: exploration rate
        self.questions        = questions
        self.ref_answers      = reference_answers
        self.max_steps        = max_steps
        self.cache_max        = cache_max
        self.similarity_model = similarity_model
        self.train_mode       = train_mode

        self._prev_obs        = np.zeros(4, dtype=np.float32)
        self.target_cache     = {}
        self.judge_cache      = {}
        self.emb_cache        = {}
        self.mut_keys         = MUT_KEYS
        self._step_counter    = 0

        self.action_space      = spaces.Discrete(len(self.mut_keys))
        self.observation_space = spaces.Box(
            low=0.0, high=1.0, shape=(4,), dtype=np.float32
        )

        self.current_template = ""
        self.last_full_prompt = ""
        self.q_idx            = 0
        self.t                = 0
        self.action_history   = []
        self.np_random        = None

    def _cache_put(self, d, k, v):
        if len(d) >= self.cache_max:
            d.pop(next(iter(d)))
        d[k] = v

    def _get_obs(self, prompt: str) -> np.ndarray:
        return self.proxy.get_obs_array(prompt)

    def _get_cached_emb(self, text: str):
        if not text: text = " "
        h = stable_hash(text)
        if h not in self.emb_cache:
            emb = self.similarity_model.encode(text, convert_to_tensor=True)
            self._cache_put(self.emb_cache, h, emb)
        return self.emb_cache[h]

    def reset(self, seed=None, options=None):
        if seed is not None:
            self.np_random, _ = gym.utils.seeding.np_random(seed)
        elif self.np_random is None:
            self.np_random, _ = gym.utils.seeding.np_random()

        self.t              = 0
        self.action_history = []
        self.q_idx = int(
            options["q_idx"] if options and "q_idx" in options
            else self.np_random.integers(0, len(self.questions))
        )

        seed_data = self.causal_generator.generate_seed_prompt(
            question=self.questions[self.q_idx],
            mode="exploratory" if self.train_mode else "greedy",
        )
        self.current_template = seed_data["prompt"]
        full_prompt = self.current_template.replace(
            "[INSERT PROMPT HERE]", self.questions[self.q_idx]
        )
        self.last_full_prompt = full_prompt
        obs = self._get_obs(full_prompt)
        self._prev_obs = obs.copy()
        return obs, {}

    def step(self, action: int):
        self.t += 1

        # ══════════════════════════════════════════════════
        # NEW v4.2: PAG-GUIDED ACTION SELECTION
        # PAG reads causal state and guides which action to take
        # This is the professor's "use causal model as guidance"
        # ══════════════════════════════════════════════════
        current_obs = self._get_obs(self.last_full_prompt)

        guided_action = self.pag_policy.select(
            obs=current_obs,
            ppo_action=int(action),
            mut_keys=self.mut_keys,
            epsilon=self.pag_epsilon,
            action_history=self.action_history,
        )
        action_name = self.mut_keys[guided_action]
        self.action_history.append(guided_action)

        self.current_template = apply_causal_mutator(
            action_name, self.current_template
        )
        full_prompt = self.current_template.replace(
            "[INSERT PROMPT HERE]", self.questions[self.q_idx]
        )
        self.last_full_prompt = full_prompt

        # Target response
        prompt_h = stable_hash(full_prompt)
        if prompt_h not in self.target_cache:
            resp = get_groq_response(
                full_prompt, TARGET_MODEL,
                max_tokens=MODEL_MAX_TOKENS["target"]
            )
            self._cache_put(self.target_cache, prompt_h, resp)
        target_response = self.target_cache[prompt_h]

        # Similarity
        emb1 = self._get_cached_emb(target_response)
        emb2 = self._get_cached_emb(self.ref_answers[self.q_idx])
        sim_score = float(util.cos_sim(emb1, emb2).item())
        is_refusal_ref = compute_ref_type(self.ref_answers[self.q_idx])
        adj_sim = (1.0 - sim_score) if is_refusal_ref else sim_score

        km_result = compute_km_success(target_response)

        is_jailbroken   = 0
        judge_pattern   = "skipped"
        relevance_score = 0.0

        if self.train_mode:
            is_jailbroken = int(km_result)
            judge_pattern = "km_proxy"
        else:
            j_key = stable_hash(
                self.questions[self.q_idx]
            ) + "_" + stable_hash(target_response)
            if j_key not in self.judge_cache:
                decision = get_judge_decision_soft(
                    self.questions[self.q_idx],
                    target_response,
                    get_groq_response,
                    JUDGE_MODEL
                )
                self._cache_put(self.judge_cache, j_key, decision)
            cached          = self.judge_cache[j_key]
            is_jailbroken   = int(cached.get("is_jailbroken", 0))
            judge_pattern   = cached.get("pattern", "unknown")
            relevance_score = float(cached.get("relevance_score", 0.0))

        new_obs = self._get_obs(full_prompt)

        total_reward = calculate_causal_reward(
            causal_obs=new_obs,
            is_jailbroken=is_jailbroken,
            adj_sim_score=adj_sim,
            prev_obs=self._prev_obs,
            action_history=self.action_history,
        )

        DYNAMIC_WEIGHTS.add(new_obs, is_jailbroken)
        self._step_counter += 1
        DYNAMIC_WEIGHTS.maybe_update(self._step_counter)

        self._prev_obs = new_obs.copy()

        terminated = bool(is_jailbroken == 1)
        truncated  = bool(self.t >= self.max_steps)

        info = {
            "sim":            sim_score,
            "adj_sim":        adj_sim,
            "action":         action_name,
            "pag_guided":     guided_action != int(action),  # NEW
            "judge_success":  terminated,
            "judge_pattern":  judge_pattern,
            "relevance_score":relevance_score,
            "km_success":     km_result,
            "prompt":         full_prompt,
            "causal_vector":  new_obs.tolist(),
        }
        return new_obs, total_reward, terminated, truncated, info

