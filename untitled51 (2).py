def get_groq_response(text: str, model_name: str,
                      temperature: float = 0.7,
                      max_tokens: int = 300,
                      max_retries: int = 10) -> str:
    if not text: return ""
    for attempt in range(max_retries):
        try:
            response = groq_client.chat.completions.create(
                model=model_name,
                messages=[{"role": "user", "content": text}],
                temperature=temperature,
                max_tokens=max_tokens,
            )
            time.sleep(MODEL_SLEEP.get(model_name, 0.3))
            return response.choices[0].message.content or ""
        except Exception as e:
            err = str(e).lower()
            if "rate" in err or "429" in err:
                wait = (2 ** (attempt + 1)) + random.uniform(1, 3)
                print(f"⚠️  Rate limit ({model_name}), waiting {wait:.1f}s")
                time.sleep(wait)
            else:
                wait = min(2 ** attempt, 30) + random.uniform(0, 2)
                print(f"⚠️  Error attempt {attempt+1}: {e}")
                time.sleep(wait)
    return ""

